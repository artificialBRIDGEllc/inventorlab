// ═══════════════════════════════════════════════════════════════════════════
// INVENTORLAB — Invention Record PDF Generator
//
// Generates a litigation-grade Invention Record PDF containing:
//   • Matter metadata + privilege banner
//   • Inventor identity block (FIDO2 signing info)
//   • Conception narrative (locked, RFC 3161 anchored)
//   • Accepted claim elements with inventorship attestations
//   • AI interaction summary (what was queried, accepted, rejected)
//   • Prior art review summary
//   • Provenance Ledger summary hash (last chain hash)
//   • Signature block placeholder (DocuSign integration in Sprint 4)
//
// Output: base64-encoded PDF string + SHA-256 hash
//
// DISCLAIMER LINE (mandatory on every page):
//   "DRAFT — For Counsel Review Only. Not a legal opinion.
//    This document was generated by InventorLab — an AI-assisted
//    invention capture platform. All outputs require independent
//    attorney review before use in patent prosecution."
// ═══════════════════════════════════════════════════════════════════════════

import { db } from "./db";
import {
  matters, users, matterInventors, conceptionNarratives,
  claimElements, priorArtReferences, provenanceLedger, inventionRecords,
} from "../shared/schema";
import { eq, and } from "drizzle-orm";
import { sha256, requestRfc3161Timestamp } from "./crypto";
import { writeLedgerEntry } from "./ledger";

// We use a pure-JS approach to generate PDF markup that can be
// rendered server-side. In production, integrate jsPDF or Puppeteer.
// For now, we generate a structured HTML document that is both
// human-readable and can be printed-to-PDF from the browser.

export interface InventionRecordData {
  matter:       any;
  inventors:    any[];
  conception:   any | null;
  claims:       any[];
  priorArt:     any[];
  ledgerEntries: any[];
  ledgerSummary: {
    totalEntries:   number;
    lastChainHash:  string | null;
    lastTsaSerial:  string | null;
    aiCallCount:    number;
    humanActionCount: number;
  };
  generatedAt:  string;
  recordHash:   string;
  tsaToken:     string | null;
  tsaSerial:    string | null;
  disclaimer:   string;
}

export async function generateInventionRecord(
  matterId:  number,
  tenantId:  number,
  requesterId: number
): Promise<InventionRecordData> {

  // ── Gather all matter data ─────────────────────────────────────────────────
  const [matter] = await db.select().from(matters).where(eq(matters.id, matterId)).limit(1);
  if (!matter) throw new Error("Matter not found");

  // Inventors
  const inventorLinks = await db.select().from(matterInventors).where(eq(matterInventors.matterId, matterId));
  const inventorUsers = await Promise.all(
    inventorLinks.map(async (link) => {
      const [u] = await db.select().from(users).where(eq(users.id, link.userId)).limit(1);
      return { ...link, user: u };
    })
  );

  // Conception narrative
  const conceptions = await db.select().from(conceptionNarratives)
    .where(and(eq(conceptionNarratives.matterId, matterId), eq(conceptionNarratives.isLocked, true)));

  // Accepted claims
  const acceptedClaims = await db.select().from(claimElements)
    .where(and(eq(claimElements.matterId, matterId), eq(claimElements.status, "accepted")))
    .orderBy(claimElements.elementNumber);

  // Prior art
  const priorArt = await db.select().from(priorArtReferences)
    .where(eq(priorArtReferences.matterId, matterId));

  // Full ledger
  const ledger = await db.select().from(provenanceLedger)
    .where(eq(provenanceLedger.matterId, matterId))
    .orderBy(provenanceLedger.id);

  const aiCalls     = ledger.filter(e => e.actorType === "ai").length;
  const humanActions = ledger.filter(e => e.actorType === "human").length;
  const lastEntry   = ledger[ledger.length - 1];

  // ── Compute record hash ────────────────────────────────────────────────────
  const recordPayload = {
    matterId:       matter.matterId,
    title:          matter.title,
    inventorCount:  inventorUsers.length,
    claimsCount:    acceptedClaims.length,
    conceptionHash: conceptions[0]?.sha256Hash ?? null,
    lastChainHash:  lastEntry?.chainHash ?? null,
    generatedAt:    new Date().toISOString(),
  };
  const recordHash  = sha256(JSON.stringify(recordPayload));
  const tsaResult   = await requestRfc3161Timestamp(recordHash);

  // ── Upsert invention record in DB ──────────────────────────────────────────
  const existingRecords = await db.select().from(inventionRecords).where(eq(inventionRecords.matterId, matterId));
  if (existingRecords.length > 0) {
    await db.update(inventionRecords).set({
      pdfSha256:    recordHash,
      tsa3161Token: tsaResult?.token ?? null,
      generatedAt:  new Date(),
      status:       "draft",
    }).where(eq(inventionRecords.matterId, matterId));
  } else {
    await db.insert(inventionRecords).values({
      matterId,
      pdfSha256:    recordHash,
      tsa3161Token: tsaResult?.token ?? null,
      status:       "draft",
    });
  }

  // ── Write to Provenance Ledger ─────────────────────────────────────────────
  await writeLedgerEntry({
    matterId, tenantId, eventType: "invention_record_generated",
    actorId: requesterId, actorType: "human",
    payload: {
      recordHash,
      tsaSerial:   tsaResult?.serial ?? null,
      claimsCount: acceptedClaims.length,
      inventorCount: inventorUsers.length,
      ledgerEntries: ledger.length,
    },
  });

  return {
    matter,
    inventors:   inventorUsers,
    conception:  conceptions[0] ?? null,
    claims:      acceptedClaims,
    priorArt,
    ledgerEntries: ledger,
    ledgerSummary: {
      totalEntries:    ledger.length,
      lastChainHash:   lastEntry?.chainHash ?? null,
      lastTsaSerial:   lastEntry?.tsaSerial ?? null,
      aiCallCount:     aiCalls,
      humanActionCount: humanActions,
    },
    generatedAt:  new Date().toISOString(),
    recordHash,
    tsaToken:     tsaResult?.token ?? null,
    tsaSerial:    tsaResult?.serial ?? null,
    disclaimer:   "DRAFT — For Counsel Review Only. Not a legal opinion. This document was generated by InventorLab, an AI-assisted invention capture platform operating under Enterprise DPA. All outputs require independent attorney review before use in patent prosecution.",
  };
}

// ── HTML renderer for the Invention Record ────────────────────────────────────
