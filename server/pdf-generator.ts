  const ts = (d: string | null) => d ? new Date(d).toLocaleString() : "—";

  const claimsHtml = claims.map(c => `
    <div class="claim-element">
      <div class="claim-number">Claim Element ${c.elementNumber}</div>
      <div class="claim-text">${escHtml(c.humanFinalText ?? c.aiProposedText ?? "")}</div>
      ${c.inventorAttestation ? `<div class="attestation">Inventor attestation: "${escHtml(c.inventorAttestation)}"</div>` : ""}
      <div class="claim-meta">
        Status: ${c.status} | Attested: ${ts(c.attestedAt)} |
        Hash: <code>${(c.sha256Hash ?? "").slice(0,16)}…</code>
        ${c.tsa3161Token ? ' | <span class="tsa-badge">TSA ✓</span>' : ""}
        ${c.isPriorityGuardFlagged ? ' | <span class="pg-flag">⚠ Priority Guard flagged</span>' : ""}
        ${c.hasDispute ? ' | <span class="dispute-flag">⚑ Dispute logged</span>' : ""}
      </div>
    </div>
  `).join("");

  const priorArtHtml = priorArt.length === 0 ? "<p>No prior art searched yet.</p>" :
    priorArt.map(p => `
      <div class="prior-art-item">
        <strong>${escHtml(p.title ?? "Untitled")}</strong>
        ${p.patentNumber ? ` <span class="patent-num">US${escHtml(p.patentNumber)}</span>` : ""}
        <span class="relevance">Relevance: ${p.relevanceScore ?? "—"}/100</span>
        ${p.isDistinguishing !== null ? `<span class="${p.isDistinguishing ? 'distinguishing' : 'not-distinguishing'}">${p.isDistinguishing ? "✓ Distinguishing" : "⚠ Not fully distinguishing"}</span>` : ""}
        ${p.annotation ? `<p class="annotation">${escHtml(p.annotation)}</p>` : ""}
      </div>
    `).join("");

